function extractStemAnalysis(explanation, keyPoint) {
  const stem = explanation.split("【選項詳解】")[0] ?? "";
  const marker = "既有解析指出：";
  const cleanKeyPoint = keyPoint.replace(/[。．.]+$/g, "");
  let facts = stem.includes(marker) ? stem.slice(stem.indexOf(marker) + marker.length) : stem;
  facts = facts
    .replace(/題幹採.*?本題題幹為：「.*?」。/s, "")
    .replace(/核心判斷重點是：.*?。/s, "")
    .replace(/\s+/g, " ")
    .replace(/。。/g, "。")
    .trim();
  return `本題重點是「${cleanKeyPoint}」。${facts}`;
}

function buildExplanation(question) {
  const reasons = rationales[question.question_number];
  if (!reasons) {
    throw new Error(`Missing rationales for Q${question.question_number}`);
  }
  const options = ["A", "B", "C", "D"]
    .map((key) => `- ${key}. ${reasons[key]}`)
    .join("\n");
  const core = question.flashcard_back || question.key_point;
  return [
    "【題幹解析】",
    extractStemAnalysis(question.explanation, question.key_point),
    "",
    "【選項詳解】",
    options,
    "",
    "【核心考點】",
    core,
  ].join("\n");
}

