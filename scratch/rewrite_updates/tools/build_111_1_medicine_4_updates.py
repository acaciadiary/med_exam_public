def load_exam():
    return json.loads(Path(SOURCE_FILE).read_text(encoding="utf-8-sig"))


def first_paragraph(explanation):
    text = (explanation or "").strip()
    if "【選項詳解】" in text:
        text = text.split("【選項詳解】", 1)[0]
    return text.replace("【題幹解析】", "").strip()


def core_point(q):
    key = q.get("key_point") or "本題核心考點"
    answers = q.get("correct_answers") or [q.get("correct_answer")]
    if len(answers) == 4:
        return f"本題有官方更正或全體給分情境，複習時要理解各選項觀念，但正式作答應以官方更正為準。核心主題：{key}"
    stem = q.get("question_text", "")
    if any(term in stem for term in ("錯誤", "不恰當", "不適當", "不是", "不包括", "最不")):
        return f"遇到否定式題幹，先確認題目是在問錯誤、例外或最不適當者，再逐一檢查每個選項的醫學敘述。核心主題：{key}"
    return f"先抓題幹最具鑑別力的線索，再連回診斷、機轉、治療或解剖定位。核心主題：{key}"


def build_explanation(q):
    qnum = q["question_number"]
    reasons = OPTION_REASONS[qnum]
    lines = ["【題幹解析】", first_paragraph(q.get("explanation")), "", "【選項詳解】"]
    for option, option_text in q["options"].items():
        lines.append(f"- {option}. {option_text}：{reasons[option]}")
    lines.extend(["", "【核心考點】", core_point(q)])
    return "\n".join(lines).strip()


def build_update(q):
    explanation = build_explanation(q)
    key_point = q.get("key_point") or ""
    update = {
        "question_id": q["id"],
        "question_number": q["question_number"],
        "explanation": explanation,
        "key_point": key_point,
        "flashcard_front": q.get("flashcard_front") or key_point,
        "flashcard_back": q.get("flashcard_back") or core_point(q),
        "flashcard_summary": q.get("flashcard_summary") or key_point,
        "review_status": "ai_generated",
        "explanation_model": "codex-high-quality-rewrite",
        "explanation_generated_at": GENERATED_AT,
        "manual_review_notes": [],
    }
    if q["question_number"] == 77:
        update["manual_review_notes"] = ["官方更正全體給分題，保留全選答案，不以單一最不適當選項教學。"]
    return update


def main():
    exam = load_exam()
    questions = exam["questions"]
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    for start in range(1, len(questions) + 1, 10):
        end = min(start + 9, len(questions))
        updates = [build_update(q) for q in questions[start - 1 : end]]
        out = {
            "source_file": SOURCE_FILE,
            "dataset_id": DATASET_ID,
            "range": {"start": start, "end": end},
            "updates": updates,
        }
        path = OUT_DIR / f"q{start:03d}-q{end:03d}.json"
        path.write_text(json.dumps(out, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"wrote {path}")


if __name__ == "__main__":
    main()
