def clean_analysis(explanation):
    head = explanation.split("【選項詳解】", 1)[0]
    head = head.replace("【題幹解析】", "").strip()
    head = re.sub(r"本題標準答案為\s*([A-D])。", r"本題官方答案是 \1。", head)
    head = head.replace("標準答案為", "官方答案是")
    return re.sub(r"\n{3,}", "\n\n", head)


