def make_update(q):
    qnum = q["question_number"]
    core = make_core(qnum)
    return {
        "question_id": q["id"],
        "question_number": qnum,
        "explanation": make_explanation(q, OPTION_DETAILS[qnum]),
        "key_point": core,
        "flashcard_front": re.sub(r"\s+", " ", q["question_text"]).strip()[:80],
        "flashcard_back": core,
        "flashcard_summary": f"{DATASET_ID} Q{qnum:03d} -> {core}",
        "review_status": "ai_generated",
        "explanation_model": "codex-high-quality-rewrite",
        "explanation_generated_at": STAMP,
        "manual_review_notes": MANUAL_REVIEW.get(qnum, []),
    }


def write_json(path, data):
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main():
    exam = json.loads(Path(SOURCE_FILE).read_text(encoding="utf-8-sig"))
    questions = exam["questions"]
    missing = sorted(set(range(1, 81)) - set(OPTION_DETAILS))
    if missing:
        raise SystemExit(f"missing option details: {missing}")

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    for start in range(1, 81, 10):
        end = start + 9
        updates = [make_update(q) for q in questions if start <= q["question_number"] <= end]
        payload = {
            "source_file": SOURCE_FILE,
            "dataset_id": DATASET_ID,
            "range": {"start": start, "end": end},
            "updates": updates,
        }
        write_json(OUT_DIR / f"q{start:03d}-q{end:03d}.json", payload)

    print(f"wrote {len(list(OUT_DIR.glob('q*.json')))} update files to {OUT_DIR}")


if __name__ == "__main__":
    main()
