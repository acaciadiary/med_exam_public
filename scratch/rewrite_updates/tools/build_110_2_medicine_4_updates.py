def build_explanation(item):
    lines = [
        "【題幹解析】",
        item["analysis"],
        "",
        "【選項詳解】",
    ]
    for option in ("A", "B", "C", "D"):
        lines.append(f"- {option}. {item['reasons'][option]}")
    lines += ["", "【核心考點】", item["core"]]
    return "\n".join(lines)


